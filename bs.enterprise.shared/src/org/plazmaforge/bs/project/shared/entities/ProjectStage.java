package org.plazmaforge.bs.project.shared.entities;

import org.plazmaforge.framework.ext.model.Dictionary;

/**
 * 
 * @author ohapon
 *
 */
public class ProjectStage extends Dictionary {

    private static final long serialVersionUID = -4184109280325889619L;
    

    private ProjectType projectType;

    private ProjectStageGroup projectStageGroup;

    private int orderNumber;

    private int averangeDuration;

    private int fromBegin;

    public int getAverangeDuration() {
	return averangeDuration;
    }

    public void setAverangeDuration(int averangeDuration) {
	this.averangeDuration = averangeDuration;
    }

    public int getFromBegin() {
	return fromBegin;
    }

    public void setFromBegin(int fromBegin) {
	this.fromBegin = fromBegin;
    }

    public int getOrderNumber() {
	return orderNumber;
    }

    public void setOrderNumber(int orderNumber) {
	this.orderNumber = orderNumber;
    }

    public ProjectStageGroup getProjectStageGroup() {
	return projectStageGroup;
    }

    public void setProjectStageGroup(ProjectStageGroup projectStageGroup) {
	this.projectStageGroup = projectStageGroup;
    }

    public ProjectType getProjectType() {
	return projectType;
    }

    public void setProjectType(ProjectType projectType) {
	this.projectType = projectType;
    }

}
