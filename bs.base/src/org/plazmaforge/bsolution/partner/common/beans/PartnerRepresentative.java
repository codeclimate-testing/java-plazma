package org.plazmaforge.bsolution.partner.common.beans;

import org.plazmaforge.framework.ext.model.BaseEntity;

/**
 * 
 * @author Oleh Hapon
 * 
 * $Id: PartnerRepresentative.java,v 1.3 2010/12/05 07:56:00 ohapon Exp $
 */
public class PartnerRepresentative extends BaseEntity {

    private IPartnerTitle partner;
    
    private String code;
    
    private String name;
    
    private String document;
    
    private String address;
    
    private String phone;
    
    

    public IPartnerTitle getPartner() {
        return partner;
    }

    public void setPartner(IPartnerTitle partner) {
        this.partner = partner;
    }

    public String getPartnerName() {
	return partner == null ? null : partner.getName();
    }
    
    
    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getDocument() {
        return document;
    }

    public void setDocument(String document) {
        this.document = document;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }
    
    
}
