public class PersonDocument extends AbstractPersonableItem {

    private PersonDocumentType personDocumentType;

    private String series;
    private String number;
    private String date;
    private String given;


    public PersonDocumentType getPersonDocumentType() {
        return personDocumentType;
    }
    
    public void setPersonDocumentType(PersonDocumentType personDocumentType) {
        this.personDocumentType = personDocumentType;
    }
    
    public String getSeries() {
        return series;
    }

    public void setSeries(String series) {
        this.series = series;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getGiven() {
        return given;
    }

    public void setGiven(String given) {
        this.given = given;
    }

    public String getPersonDocumentTypeName() {
        return getPersonDocumentType() == null ? null : getPersonDocumentType().getName();
    }

}
