package org.plazmaforge.bs.project.shared.entities;

import java.util.Date;
import org.plazmaforge.framework.ext.model.BaseEntity;


/**
 * 
 * @author ohapon
 *
 */
public class ProjectStageLink  extends BaseEntity {

    private static final long serialVersionUID = 8623884827857038217L;
    

    private Project project;
    
    private ProjectStage projectStage;
    
    private Date startDate;
    
    private Date endDate;
    
    private Date actualStartDate;
    
    private Date actualEndDate;

    private String name;
    
    private String description;
    
    public Date getActualEndDate() {
        return actualEndDate;
    }

    public void setActualEndDate(Date actualEndDate) {
        this.actualEndDate = actualEndDate;
    }

    public Date getActualStartDate() {
        return actualStartDate;
    }

    public void setActualStartDate(Date actualStartDate) {
        this.actualStartDate = actualStartDate;
    }

    public Date getEndDate() {
        return endDate;
    }

    public void setEndDate(Date endDate) {
        this.endDate = endDate;
    }

    public Project getProject() {
        return project;
    }

    public void setProject(Project project) {
        this.project = project;
    }

    public ProjectStage getProjectStage() {
        return projectStage;
    }

    public void setProjectStage(ProjectStage projectStage) {
        this.projectStage = projectStage;
    }
    
    public String getProjectStageName() {
        return projectStage == null ? null : projectStage.getName();
    }

    public Date getStartDate() {
        return startDate;
    }

    public void setStartDate(Date startDate) {
        this.startDate = startDate;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
    

    
    

}
